class DataType{
	public static void main(String args[]){
		System.out.println("\n----------------RANGE OF DATA TYPES----------------\n");
		System.out.println("\tByte  minimum  value = " + Byte.MIN_VALUE);
		System.out.println("\tByte  maximum  value = " + Byte.MAX_VALUE);
		System.out.println();
		System.out.println("\tShort  minimum value = " + Short.MIN_VALUE);
		System.out.println("\tShort  maximum value = " + Short.MAX_VALUE);
		System.out.println();
		System.out.println("\tInt   minimum  value = " + Integer.MIN_VALUE);
		System.out.println("\tInt   maximum  value = " + Integer.MAX_VALUE);
		System.out.println();
		System.out.println("\tLong  minimum  value = " + Long.MIN_VALUE);
		System.out.println("\tLong  maximum  value = " + Long.MAX_VALUE);
		System.out.println();
		System.out.println("\tFloat  minimum value = " + Float.MIN_VALUE);
		System.out.println("\tFloat  maximum value = " + Float.MAX_VALUE);
		System.out.println();
		System.out.println("\tDouble minimum value = " + Double.MIN_VALUE);
		System.out.println("\tDouble maximum value = " + Double.MAX_VALUE);
		System.out.println();
	}
}